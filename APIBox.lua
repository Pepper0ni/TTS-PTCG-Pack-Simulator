function getSet(obj,color,alt)
 local setPos=self.GetPosition()
 setPos.x=setPos.x+3
 local setName=setData[curSet].setName
 local spawnPos=self.positionToWorld({0,1,7})
 setCache=Global.getTable("PPacksCache["..setName.."]")

 if setCache and setCache.cache then
   setDeck=spawnObjectData({data=setCache.cache,position=spawnPos,rotation=self.GetRotation()})
 else
  if not setCache or not setCache.loading then
   r={}
   decoded={}
   local orderText="&orderBy=number"
   if setData[curSet].unordered then orderText=''end
   local callPerSet=settings.APICalls
   local loadingNum=callPerSet
   if setData[curSet].subSet then
    callPerSet=math.ceil(settings.APICalls/2)
    loadingNum=callPerSet*2
   end
   if setData[curSet].SMEnergy then loadingNum=loadingNum+1 end
   Global.setTable("PPacksCache["..setName.."]",{loading=loadingNum,cache=nil})
   local count=requestSet(1,callPerSet,setData[curSet].setID,setName,setData[curSet].size or 300,orderText,color)
   if setData[curSet].subSet then count=requestSet(count,callPerSet,setData[curSet].subSet.setID,setName,setData[curSet].subSet.size or 300,orderText,color)end
   if setData[curSet].SMEnergy then requestSMEnergy(count,setName,color)end
  else
   broadcastToColor("Loading Cards, Please Wait",color,{0,1,0})
  end
 end
end

function requestSet(count,calls,setIDToLoad,setName,size,orderText,color)
 for c=1,calls do
  local page=count
  r[count]=WebRequest.get('https://api.pokemontcg.io/v2/cards?q=!set.id:"'..setIDToLoad..'"&page='..tostring(c)..'&pageSize='..tostring(math.ceil(size/calls))..orderText.."&select=id,name,images,number,rarity,set,supertype,subtypes,types,nationalPokedexNumbers",||cacheSet(r[page],setName,color,page))
  count=count+1
 end
 return count
end

function requestSMEnergy(count,setName,color)
 local page=count
 r[count]=WebRequest.get("https://api.pokemontcg.io/v2/cards?q=number:%5B164%20TO%20172%5D%20!set.id:sm1&order_by=number&select=id,name,images,number,rarity,set,supertype,subtypes,types,nationalPokedexNumbers",||cacheSet(r[page],setName,color,page))
 count=count+1
 return count
end

function cacheSet(request,setName,color,page)
 local cache=Global.GetTable("PPacksCache["..setName.."]")
 if request.is_error or request.response_code>=400 then
  log(request.error)
  log(request.text)
  log(request.response_code)
  broadcastToColor("Error: "..tostring(request.response_code),color,{1,0,0})
  Global.setTable("PPacksCache["..setName.."]",{loading=nil,cache=nil})
 elseif cache.loading>=1 then
  decoded[page]=json.parse(string.gsub(request.text,"\\u0026","&"))
--credit to dzikakulka and Larikk
--use the below line in the parse instead if this line of code ever breaks
--string.gsub(request.text,[[\u([0-9a-fA-F]+)]],|s|return([[\u{%s}]]):format(s))
  if cache.loading==1 then
   local spawnPos=self.positionToWorld({0,1,7})
   local curCard=1
   local deckData=
    {Name="Deck",
    Transform={posX=spawnPos[1],posY=spawnPos[2],posZ=spawnPos[3],rotX=0,rotY=0,rotZ=0,scaleX=1,scaleY=1,scaleZ=1},
    DeckIDs={},
    CustomDeck={},
    ContainedObjects={}
   }
   for a=1,#decoded do
    local cardData=decoded[a].data
    for b=1,#cardData do
     local DeckID=999+curCard
     local customData=getCustomData(cardData[b])
     deckData.DeckIDs[curCard]=DeckID*100
     deckData.CustomDeck[DeckID]=customData
     deckData.ContainedObjects[curCard]=getCardData(deckData.Transform,cardData[b],customData,DeckID*100,DeckID)
     deckData.ContainedObjects[curCard]["GUID"]=tostring(123456+curCard)
     curCard=curCard+1
    end
   end
   spawnObjectData({data=deckData,rotation=self.GetRotation()})
   Global.setTable("PPacksCache["..setName.."]",{loading=nil,cache=deckData})
  else
   Global.SetTable("PPacksCache["..setName.."]",{loading=cache.loading-1,cache=nil})
  end
 end
end

function getCardData(spawnLoc,cardData,customData,cardID,deckID)
 local cardType=enumTable(subTypeNums[cardData.supertype]or 0,cardData.subtypes,subTypeNums,0,0)
 local monType=enumTable(0,cardData.types,TypeNums,10,200)
 if monType==0 then monType=500 end
 local rar=""
 if cardData.rarity then
  rar=" "..string.gsub(cardData.rarity,"[^%u]","")
 end
 return{Name="CardCustom",
 Transform=spawnLoc,
 Nickname=cardData.name,
 Description=cardData.set.name.." #"..cardData.number..rar,
 GMNotes=tostring(cardType)..convertNatDex(cardData.nationalPokedexNumbers,cardData.subtypes),
 Memo=string.gsub(cardData.set.releaseDate,"/","")..buildCardNumber(cardData.number),
 CardID=cardID,
 CustomDeck={[deckID]=customData},
 LuaScriptState=tostring(monType)
}
end

