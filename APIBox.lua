function GetScriptAddOn(packData,boxID,packNum)
 local scriptAddOn='dropSlots='..packData.dropSlots..'\npullRates='..packData.pullRates..'\nsetName="'..setData[curSet].setName..'"\nsetSize='..tostring(setData[curSet].size or 300)..'\nsetID="'..setData[curSet].setID..'"\n'
 if setData[curSet].unordered then
  scriptAddOn=scriptAddOn..'setUnordered=true\n'
 end
 if setData[curSet].subSet then
  scriptAddOn=scriptAddOn..'subSetSize='..tostring(setData[curSet].subSet.size or 300)..'\nsubSetID="'..setData[curSet].subSet.setID..'"\n'
 end
 if setData[curSet].SMEnergy then
  scriptAddOn=scriptAddOn..'SMEnergy=true\n'
 end
 if setData[curSet].BSEnergy then
  scriptAddOn=scriptAddOn..'BSEnergy=true\n'
 end
 if setData[curSet].SVEnergy then
  scriptAddOn=scriptAddOn..'SVEnergy=true\n'
 end
 if setData[curSet].VStar then
  scriptAddOn=scriptAddOn..'VStar=true\n'
 end
 if boxID then
  scriptAddOn=scriptAddOn..'boxPulls='..packData.boxPulls..'\nboxID='..tostring(boxID)..'\npackNum='..tostring(packNum)..'\n'
 end
 if packData.godChance then
  scriptAddOn=scriptAddOn..'godChance='..tostring(packData.godChance)..'\ngodPacks='..tostring(packData.godPacks)..'\n'
 end
 if packData.godSlot then
  scriptAddOn=scriptAddOn..'godSlot='..tostring(packData.godSlot)..'\n'
 end
 return scriptAddOn
end

