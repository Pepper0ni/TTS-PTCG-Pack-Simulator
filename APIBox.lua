 local custom=setData[curSet].custom
 custom.mesh=getSteamUrl(custom.mesh)
 custom.diffuse=getSteamUrl(custom.diffuse)
 custom.normal=getSteamUrl(custom.normal) or""
 custom.collider=custom.mesh or""
 self.setCustomObject(custom)
 self.setDescription(setData[curSet].setName)
 saveData()
 self.reload()
end

